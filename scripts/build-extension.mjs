#!/usr/bin/env node
/**
 * Build Fluxpage Chrome extension: generates config.generated.js and optional prod manifest.
 * Usage: node scripts/build-extension.mjs [--dev]
 */
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, "..");
const extDir = path.join(root, "extension");

const isDev = process.argv.includes("--dev");

const apiBase =
  process.env.EXTENSION_API_BASE ||
  "https://stoic-caiman-320.convex.site";
const webBase = isDev
  ? process.env.EXTENSION_WEB_BASE || "http://localhost:3000"
  : process.env.EXTENSION_WEB_BASE || "https://www.fluxpage.com";
const localPdf = isDev
  ? process.env.EXTENSION_LOCAL_PDF_API || "http://localhost:8000"
  : process.env.EXTENSION_LOCAL_PDF_API || "";

const configJs = `// Auto-generated by scripts/build-extension.mjs — do not edit
globalThis.__RESUMOD_CONFIG__ = {
  API_BASE: ${JSON.stringify(apiBase)},
  WEB_BASE: ${JSON.stringify(webBase)},
  LOCAL_PDF_API: ${JSON.stringify(localPdf)},
};
`;

fs.writeFileSync(path.join(extDir, "config.generated.js"), configJs, "utf8");

const manifestPath = path.join(extDir, "manifest.json");
const manifest = JSON.parse(fs.readFileSync(manifestPath, "utf8"));

const prodHosts = [
  "https://www.fluxpage.com/*",
  "https://fluxpage.com/*",
  "https://*.convex.site/*",
  "https://*.convex.cloud/*",
];
const devHosts = [
  "http://localhost:8000/*",
  "http://localhost:3000/*",
  "https://*.convex.site/*",
  "https://*.convex.cloud/*",
];

manifest.host_permissions = [
  ...(isDev ? devHosts : prodHosts),
  "https://www.linkedin.com/*",
  "https://*.linkedin.com/*",
  "https://internshala.com/*",
  "https://*.internshala.com/*",
  "https://www.naukri.com/*",
  "https://*.naukri.com/*",
  "https://www.indeed.com/*",
  "https://*.indeed.com/*",
  "https://www.glassdoor.com/*",
  "https://*.glassdoor.com/*",
];

const connectSrc = [
  "'self'",
  ...(isDev
    ? ["http://localhost:8000", "http://localhost:3000"]
    : ["https://www.fluxpage.com", "https://fluxpage.com"]),
  "https://*.convex.site",
  "https://*.convex.cloud",
  "https://unpkg.com",
]
  .filter(Boolean)
  .join(" ");

manifest.content_security_policy = {
  extension_pages: `script-src 'self' 'wasm-unsafe-eval'; object-src 'self'; connect-src ${connectSrc}`,
};

// Prepend config to every content script bundle
for (const cs of manifest.content_scripts || []) {
  if (cs.js && !cs.js.includes("config.generated.js")) {
    cs.js = ["config.generated.js", ...cs.js];
  }
}

fs.writeFileSync(manifestPath, JSON.stringify(manifest, null, 2) + "\n", "utf8");

console.log(`Extension built (${isDev ? "dev" : "prod"}):`);
console.log(`  API_BASE: ${apiBase}`);
console.log(`  WEB_BASE: ${webBase}`);
console.log(`  Output: extension/config.generated.js`);
